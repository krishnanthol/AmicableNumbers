public class Amicable
{
    private int firstNum;
    private int secondNum;

    public Amicable(int firstNum, int secondNum)
    {
        firstNum = this.firstNum;
        secondNum = this.secondNum;
    }

    public int getFirstNum()
    {
        return firstNum;
    }

    public int getSecondNum()
    {
        return secondNum;
    }
}
